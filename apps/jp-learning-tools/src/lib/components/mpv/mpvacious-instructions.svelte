    <h3 class="text-xl font-semibold mb-4">Menu options</h3>

    <p class="mb-4">Advanced menu has the following options:</p>

    <ul class="list-disc list-inside space-y-3 ml-2">
      <li>
        <kbd
          class="px-2 py-1 text-xs font-semibold text-gray-800 bg-gray-100 border border-gray-200 rounded dark:bg-gray-600 dark:text-gray-100 dark:border-gray-500"
          >i</kbd
        >
        - Cycle between display modes.
      </li>
      <li>
        <kbd
          class="px-2 py-1 text-xs font-semibold text-gray-800 bg-gray-100 border border-gray-200 rounded dark:bg-gray-600 dark:text-gray-100 dark:border-gray-500"
          >v</kbd
        >
        - Open subtitle selection menu. Upon exit, this menu exports selected subtitles
        into Advanced menu.
      </li>
      <li>
        <kbd
          class="px-2 py-1 text-xs font-semibold text-gray-800 bg-gray-100 border border-gray-200 rounded dark:bg-gray-600 dark:text-gray-100 dark:border-gray-500"
          >f</kbd
        >
        - Increment number of cards to update. Only affects note updating, including
        quick card creation. The number of cards to update is reset to 1 upon updating
        a note.
      </li>
      <li>
        <kbd
          class="px-2 py-1 text-xs font-semibold text-gray-800 bg-gray-100 border border-gray-200 rounded dark:bg-gray-600 dark:text-gray-100 dark:border-gray-500"
          >shift+f</kbd
        >
        - Decrement number of cards to update.
      </li>
      <li>
        <kbd
          class="px-2 py-1 text-xs font-semibold text-gray-800 bg-gray-100 border border-gray-200 rounded dark:bg-gray-600 dark:text-gray-100 dark:border-gray-500"
          >c</kbd
        >
        - Interactive subtitle selection. The range of the currently displayed subtitle
        line is selected. The selection then grows both ways based on the following
        displayed lines. It does nothing if there are no subs on screen.
      </li>
      <li>
        <kbd
          class="px-2 py-1 text-xs font-semibold text-gray-800 bg-gray-100 border border-gray-200 rounded dark:bg-gray-600 dark:text-gray-100 dark:border-gray-500"
          >shift+s</kbd
        >
        - Set the start time to the current sub. The selection then grows forward
        based on the following displayed lines. The default selection spans across
        the range of the currently displayed subtitle line.
      </li>
      <li>
        <kbd
          class="px-2 py-1 text-xs font-semibold text-gray-800 bg-gray-100 border border-gray-200 rounded dark:bg-gray-600 dark:text-gray-100 dark:border-gray-500"
          >shift+e</kbd
        >
        - Set the end time to the current sub. The selection then grows backward
        based on the following displayed lines. The default selection spans across
        the range of the currently displayed subtitle line.
      </li>
    </ul>

    <p class="my-3">
      Then seek with
      <kbd
        class="px-2 py-1 text-xs font-semibold text-gray-800 bg-gray-100 border border-gray-200 rounded dark:bg-gray-600 dark:text-gray-100 dark:border-gray-500"
        >Shift+h</kbd
      >
      and
      <kbd
        class="px-2 py-1 text-xs font-semibold text-gray-800 bg-gray-100 border border-gray-200 rounded dark:bg-gray-600 dark:text-gray-100 dark:border-gray-500"
        >Shift+l</kbd
      >
      to the previous/next line that you want to add. Press
      <kbd
        class="px-2 py-1 text-xs font-semibold text-gray-800 bg-gray-100 border border-gray-200 rounded dark:bg-gray-600 dark:text-gray-100 dark:border-gray-500"
        >n</kbd
      >
      to make the card.
    </p>

    <ul class="list-disc list-inside space-y-3 ml-2">
      <li>
        <kbd
          class="px-2 py-1 text-xs font-semibold text-gray-800 bg-gray-100 border border-gray-200 rounded dark:bg-gray-600 dark:text-gray-100 dark:border-gray-500"
          >r</kbd
        >
        - Forget all previously saved timings and associated dialogs.
      </li>
      <li>
        <kbd
          class="px-2 py-1 text-xs font-semibold text-gray-800 bg-gray-100 border border-gray-200 rounded dark:bg-gray-600 dark:text-gray-100 dark:border-gray-500"
          >z</kbd
        >
        and
        <kbd
          class="px-2 py-1 text-xs font-semibold text-gray-800 bg-gray-100 border border-gray-200 rounded dark:bg-gray-600 dark:text-gray-100 dark:border-gray-500"
          >Shift+z</kbd
        >
        - Adjust subtitle delay.
      </li>
    </ul>

    <p class="my-3">If above fails, you have to manually set timings.</p>

    <ul class="list-disc list-inside space-y-3 ml-2">
      <li>
        <kbd
          class="px-2 py-1 text-xs font-semibold text-gray-800 bg-gray-100 border border-gray-200 rounded dark:bg-gray-600 dark:text-gray-100 dark:border-gray-500"
          >s</kbd
        >
        - Set the start time. The selection then grows forward based on the following
        displayed lines. The default selection spans across the selected start point
        and the end of the subtitle line.
      </li>
      <li>
        <kbd
          class="px-2 py-1 text-xs font-semibold text-gray-800 bg-gray-100 border border-gray-200 rounded dark:bg-gray-600 dark:text-gray-100 dark:border-gray-500"
          >e</kbd
        >
        - Set the end time. The selection then grows backward based on the following
        displayed lines. The default selection spans across the selected end point
        and the start of the subtitle line.
      </li>
    </ul>

    <p class="mt-3">
      Then, as earlier, press
      <kbd
        class="px-2 py-1 text-xs font-semibold text-gray-800 bg-gray-100 border border-gray-200 rounded dark:bg-gray-600 dark:text-gray-100 dark:border-gray-500"
        >n</kbd
      >
      to make the card.
